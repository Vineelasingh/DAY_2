import java.util.*;
public class logic {

	public static void main(String[] args) {
	System.out.println("enter n value");
	int i,j,n;
	Scanner sc=new Scanner(System.in);
	n=sc.nextInt();
	for(i=0;i<n;i++)
	{
		for(j=0;j<n;j++)
		{
			System.out.println("*");
		}
		System.out.println("*");
	}

	}

}
